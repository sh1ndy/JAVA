
public class SpecialCharEx {

	public static void main(String[] args) {

		System.out.println('\'');
		System.out.println("abc\t123\b456"); // b에 의해서 3이 저워진다.
		System.out.println("\n"); // new line 출력 하고 개
		System.out.println("\"Hello\""); // 큰 따옴표를 출력하려면 이렇게 다.
		System.out.println("c:\\");
		
	}

}
